    public void apagar(){
        foneint = new Intra();
        try{
            foneint.setIdProduto(Integer.parseInt(ctID.getText()));
            foneint = BDInt.consultaIDIntra(foneint);
        
            if(foneint != null){
                ctID.setText(Integer.toString(foneint.getIdProduto()));
                ctPreco.setText(Float.toString(foneint.getPreco()));
                ctMarca.setText(foneint.getMarca());
                ctEntrada.setText(foneint.getConexao());
                int x = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esse Produto do Banco de dados?", "Exclusão", JOptionPane.YES_NO_OPTION);
                if(x == 0){
                    BDInt.apagarBDInt(foneint);
                    JOptionPane.showMessageDialog(null, "Produto apagado com sucesso!", "Sucesso!", JOptionPane.DEFAULT_OPTION);
                    limpar();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    public void alterar(){
        foneint = new Intra();
        try{
            foneint.setIdProduto(Integer.parseInt(ctID.getText()));
            foneint = BDInt.alterarBDInt(foneint);
        
            if(foneint != null){
                ctID.setText(Integer.toString(foneint.getIdProduto()));
                ctPreco.setText(Float.toString(foneint.getPreco()));
                ctMarca.setText(foneint.getMarca());
                ctEntrada.setText(foneint.getConexao());
                JOptionPane.showMessageDialog(null, "Dados atualizados!.", "Sucesso!", JOptionPane.DEFAULT_OPTION);
                limpar();
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    public void listarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaIntra.getModel();
        int indice = 0;
        
        modelo.setRowCount(indice);
        for(Intra foneint : BDInt.getBDInt()){
            modelo.insertRow(indice, new Object[]{foneint.getIdProduto(), foneint.getPreco(), foneint.getMarca(), foneint.getConexao()});
            indice++;
        }
    }
    
    public void consultar(){
        foneint = new Intra();
        try{
            foneint.setIdProduto(Integer.parseInt(ctID.getText()));
            foneint = BDInt.consultaIDIntra(foneint);
        
            if(foneint != null){
                ctID.setText(Integer.toString(foneint.getIdProduto()));
                ctPreco.setText(Float.toString(foneint.getPreco()));
                ctMarca.setText(foneint.getMarca());
                ctEntrada.setText(foneint.getConexao());
                JOptionPane.showMessageDialog(null, "Confira os dados.", "Encontrado!", JOptionPane.DEFAULT_OPTION);
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void cadastrarFoneInt(){
        foneint = new Intra();
        try {
            foneint.setPreco(Float.parseFloat(ctPreco.getText()));
            foneint.setIdProduto(Integer.parseInt(ctID.getText()));
            foneint.setMarca(ctMarca.getText());
            foneint.setConexao(ctEntrada.getText());
            
            
            foneint = BDInt.setBDInt(foneint);
            if(foneint != null){
                JOptionPane.showMessageDialog(null, "Cadastrado feito corretamente.", "Sucesso", JOptionPane.DEFAULT_OPTION);
                limpar();
            }else{
                JOptionPane.showMessageDialog(null, "Ja existe um fone com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(PrecoException ex){
            JOptionPane.showMessageDialog(null, "Preencha os valores corretamente", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void limpar(){
        ctID.setText("");
        ctPreco.setText("");
        ctMarca.setText("");
        ctEntrada.setText("");
        ctID.requestFocus();
    }
    
