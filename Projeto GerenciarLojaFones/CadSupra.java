    public void apagar(){
        fonesup = new Supra();
        try{
            fonesup.setIdProduto(Integer.parseInt(ctID.getText()));
            fonesup = BDSup.consultaIDSupra(fonesup);
        
            if(fonesup != null){
                ctID.setText(Integer.toString(fonesup.getIdProduto()));
                ctPreco.setText(Float.toString(fonesup.getPreco()));
                ctMarca.setText(fonesup.getMarca());
                ctPeso.setText(Integer.toString(fonesup.getPeso()));
                int x = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esse Produto do Banco de dados?", "Exclusão", JOptionPane.YES_NO_OPTION);
                if(x == 0){
                    BDSup.apagarBDSup(fonesup);
                    JOptionPane.showMessageDialog(null, "Produto apagado com sucesso!", "Sucesso!", JOptionPane.DEFAULT_OPTION);
                    limpar();
                }
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        } 
    }
    
    public void alterar(){
        fonesup = new Supra();
        try{
            fonesup.setIdProduto(Integer.parseInt(ctID.getText()));
            fonesup = BDSup.alterarBDSup(fonesup);
        
            if(fonesup != null){
                ctID.setText(Integer.toString(fonesup.getIdProduto()));
                ctPreco.setText(Float.toString(fonesup.getPreco()));
                ctMarca.setText(fonesup.getMarca());
                ctPeso.setText(Integer.toString(fonesup.getPeso()));
                JOptionPane.showMessageDialog(null, "Dados atualizados!.", "Sucesso!", JOptionPane.DEFAULT_OPTION);
                limpar();
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    public void listarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tabelaSupra.getModel();
        int indice = 0;
        
        modelo.setRowCount(indice);
        for(Supra fonesup : BDSup.getBDSup()){
            modelo.insertRow(indice, new Object[]{fonesup.getIdProduto(), fonesup.getPreco(), fonesup.getMarca(), fonesup.getPeso()});
            indice++;
        }
    }
    
    public void consultar(){
        fonesup = new Supra();
        try{
            fonesup.setIdProduto(Integer.parseInt(ctID.getText()));
            fonesup = BDSup.consultaIDSupra(fonesup);
        
            if(fonesup != null){
                ctID.setText(Integer.toString(fonesup.getIdProduto()));
                ctPreco.setText(Float.toString(fonesup.getPreco()));
                ctMarca.setText(fonesup.getMarca());
                ctPeso.setText(Integer.toString(fonesup.getPeso()));
                JOptionPane.showMessageDialog(null, "Confira os dados.", "Encontrado!", JOptionPane.DEFAULT_OPTION);
            }else{
                JOptionPane.showMessageDialog(null, "Não foi encontrado um produto com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
                limpar();
            }
        }catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "O ID só deve ser do tipo inteiro!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void cadastrarFonesup(){
        fonesup = new Supra();
        try {
            fonesup.setPreco(Float.parseFloat(ctPreco.getText()));
            fonesup.setIdProduto(Integer.parseInt(ctID.getText()));
            fonesup.setMarca(ctMarca.getText());
            fonesup.setPeso(Integer.parseInt(ctPeso.getText()));
            
            
            fonesup = BDSup.setBDSup(fonesup);
            if(fonesup != null){
                JOptionPane.showMessageDialog(null, "Cadastrado feito corretamente.", "Sucesso", JOptionPane.DEFAULT_OPTION);
                limpar();
            }else{
                JOptionPane.showMessageDialog(null, "Ja existe um fone com esse ID!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            
        }catch(PrecoException ex){
            JOptionPane.showMessageDialog(null, "Preencha os valores corretamente", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void limpar(){
        ctID.setText("");
        ctPreco.setText("");
        ctMarca.setText("");
        ctPeso.setText("");
        ctID.requestFocus();
    }
    
    public void sair(){
           int resposta = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja sair?", "SAIR?", JOptionPane.YES_NO_OPTION);
           if (resposta ==0){
               this.dispose();
           }
    }
